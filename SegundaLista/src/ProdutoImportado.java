
public class ProdutoImportado {
	private String tipo;
	private float preco;
	
	public void setTipo(String novoTipo) {
		tipo = novoTipo;	
	}
	
	public String getTipo() {
		return tipo;
	}
	
	public void setPreco(float novoPreco) {
		preco = novoPreco;
	}
	public float getPreco() {
		return preco;
	}
}
